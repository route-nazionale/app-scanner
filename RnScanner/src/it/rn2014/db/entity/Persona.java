package it.rn2014.db.entity;




public class Persona {
	
	private String nome ;
	private String cognome ;
	private String ruolo ;
	private String codiceAgesci ;
	private String gruppo ;
	private String codiceBadge ;
	
	
	
	public String getCodiceBadge() {
		return codiceBadge;
	}
	public void setCodiceBadge(String codiceBadge) {
		this.codiceBadge = codiceBadge;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public String getRuolo() {
		return ruolo;
	}
	public void setRuolo(String ruolo) {
		this.ruolo = ruolo;
	}
	public String getCodiceAgesci() {
		return codiceAgesci;
	}
	public void setCodiceAgesci(String codiceAgesci) {
		this.codiceAgesci = codiceAgesci;
	}
	public String getGruppo() {
		return gruppo;
	}
	public void setGruppo(String gruppo) {
		this.gruppo = gruppo;
	}
	
	

}
	

